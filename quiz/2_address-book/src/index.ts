interface PhoneNumberDictionary {
  [phone: string]: {
    num: number;
  };
}

interface Contact {
  name: string;
  address: string;
  phones: PhoneNumberDictionary;
}


// enum으로 타입을 정의하기
enum PhoneType {
  Home = 'home',
  Office = 'office',
  Studio = 'studio'
}

